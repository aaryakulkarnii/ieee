import os
import json
import uuid
import random
from faker import Faker
import datetime

fake = Faker()

def generate_cicids2017_log(is_malicious):
    ip_src = fake.ipv4()
    ip_dst = fake.ipv4()
    port_src = random.randint(1024, 65535)
    port_dst = random.choice([80, 443, 22, 21, 3389]) if not is_malicious else random.randint(1024, 65535)
    protocol = random.choice([6, 17]) # TCP, UDP
    timestamp = fake.date_time_this_year().strftime("%d/%m/%Y %H:%M:%S")
    
    # Simple flow representation
    flow = f"{ip_src},{port_src},{ip_dst},{port_dst},{protocol},{timestamp}"
    return flow

def generate_hdfs_log(is_malicious):
    block_id = f"blk_{random.randint(1000000000, 9999999999)}_{random.randint(1000, 9999)}"
    timestamp = fake.date_time_this_year().strftime("%y%m%d %H%M%S")
    pid = random.randint(10, 9999)
    
    if is_malicious:
        msgs = [
            f"Receiving block {block_id} src: /10.251.43.115:55086 dest: /10.251.43.115:50010 failed",
            f"Exception in receiveBlock for {block_id}",
            f"PacketResponder {block_id} Exception java.io.EOFException"
        ]
    else:
        msgs = [
            f"Receiving block {block_id} src: /10.251.43.115:55086 dest: /10.251.43.115:50010",
            f"Received block {block_id} of size 67108864 from /10.251.43.115",
            f"PacketResponder {block_id} for block {block_id} terminating"
        ]
        
    return f"{timestamp} {pid} INFO dfs.DataNode$DataXceiver: {random.choice(msgs)}"

def generate_bgl_log(is_malicious):
    timestamp = fake.date_time_this_year().strftime("%Y-%m-%d-%H.%M.%S.%f")
    node = f"R{random.randint(0, 7)}{random.randint(0, 7)}-M{random.randint(0, 1)}-N{random.randint(0, 9)}-C:J{random.randint(0, 16)}-U{random.randint(0, 1)}"
    
    if is_malicious:
        msgs = [
            f"FATAL machine check interrupt (Machine Check)",
            f"ERROR generated by core {random.randint(0, 1)} processor",
            f"SEVERE DDR error detected"
        ]
    else:
        msgs = [
            f"INFO core {random.randint(0, 1)} processor running",
            f"INFO generation of core {random.randint(0, 1)} complete",
            f"INFO L3 EDRAM error detected and corrected"
        ]
        
    level = "FATAL" if is_malicious else "INFO"
    return f"- {timestamp} {node} RAS KERNEL {level} {random.choice(msgs)}"

def format_log(raw_log, fmt):
    if fmt == "syslog":
        timestamp = fake.date_time_this_year().strftime("%b %d %H:%M:%S")
        host = fake.hostname()
        return f"<{random.randint(0, 191)}>{timestamp} {host} system[123]: {raw_log}"
    elif fmt == "CEF":
        return f"CEF:0|Vendor|Product|1.0|100|Log Event|1|msg={raw_log}"
    elif fmt == "JSON":
        return json.dumps({"timestamp": fake.iso8601(), "message": raw_log})
    return raw_log

def create_dataset():
    data_dir = os.path.join(os.path.dirname(__file__), 'processed')
    os.makedirs(data_dir, exist_ok=True)
    
    output_file = os.path.join(data_dir, 'dataset.jsonl')
    
    # 700 benign, 500 malicious, 300 adversarially crafted
    records = []
    
    def add_record(is_malicious, is_adversarial, goal, attacker_level, tactic):
        source = random.choice(["CICIDS2017", "HDFS", "BGL"])
        fmt = random.choice(["syslog", "CEF", "JSON"])
        
        if source == "CICIDS2017":
            raw = generate_cicids2017_log(is_malicious)
        elif source == "HDFS":
            raw = generate_hdfs_log(is_malicious)
        else:
            raw = generate_bgl_log(is_malicious)
            
        formatted_log = format_log(raw, fmt)
        
        # We don't apply the actual attack text here, that will be done by the attack scripts.
        # But we mark them as intended for adversarial crafting.
        if is_adversarial:
            label = "malicious" # Adversarial logs are originally malicious
        else:
            label = "malicious" if is_malicious else "benign"
            
        record = {
            "log_id": str(uuid.uuid4()),
            "raw_log": formatted_log,
            "format": fmt,
            "ground_truth_label": label,
            "attack_type": tactic if is_malicious or is_adversarial else "null",
            "is_adversarial": is_adversarial,
            "attack_goal": goal if is_adversarial else "null",
            "attacker_level": attacker_level if is_adversarial else "null",
            "source_dataset": source
        }
        records.append(record)

    # 700 benign
    for _ in range(700):
        add_record(is_malicious=False, is_adversarial=False, goal="null", attacker_level="null", tactic="null")
        
    # 500 malicious
    tactics = ["Initial Access", "Execution", "Persistence", "Privilege Escalation", "Defense Evasion", "Credential Access", "Discovery", "Lateral Movement", "Collection", "Command and Control", "Exfiltration", "Impact"]
    for _ in range(500):
        add_record(is_malicious=True, is_adversarial=False, goal="null", attacker_level="null", tactic=random.choice(tactics))
        
    # 300 adversarially crafted (will be injected by attack scripts, currently we just set flags and raw log)
    # The attacks scripts will take this dataset, filter for is_adversarial=True, and apply the injection.
    for i in range(300):
        goal = "evasion" if i % 2 == 0 else "poisoning"
        level = "blackbox" if i % 2 == 0 else "greybox"
        add_record(is_malicious=True, is_adversarial=True, goal=goal, attacker_level=level, tactic=random.choice(tactics))
        
    random.shuffle(records)
    
    with open(output_file, 'w', encoding='utf-8') as f:
        for r in records:
            f.write(json.dumps(r) + '\n')
            
    print(f"Generated {len(records)} records at {output_file}")

if __name__ == "__main__":
    create_dataset()
